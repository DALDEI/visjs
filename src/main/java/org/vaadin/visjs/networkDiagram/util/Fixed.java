package org.vaadin.visjs.networkDiagram.util;


public class Fixed {
    boolean x = false;
    boolean y = false;

    public boolean isX() {
        return x;
    }

    public void setX(boolean x) {
        this.x = x;
    }

    public boolean isY() {
        return y;
    }

    public void setY(boolean y) {
        this.y = y;
    }
}
